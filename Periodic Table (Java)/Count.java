public class Count
{
    public static int countI(Element inArray[][])
    {

        int i;
        i=0;

        if(i < 10)
        {
            while (!(inArray[i][0] == (null)))
            {
                {
                    i++;

                }
            }
        }
        else
        {
            i=0;
        }

        return i;
    }


    public static int countJ(Element inArray[][])
    {

        int j;
        j=0;

        if(j < 10)
        {
            while (!(inArray[0][j] == (null)))
            {
                {
                    j++;
                }
            }
        }
        else
        {
            j=0;
        }
        return j;

    }
}
