    private void run_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_run_ButtonActionPerformed
        // TODO add your handling code here:
        /* IF THERE IS AN ISSUE MAKE SURE THERE IS A SPACE " " BETWEEN EACH TAG */
        
        // Windows cmd output to a file isnt working correctly
        String exe = command + tags[0] + tags[1] + tags[2] + tags[3];
        System.out.println(exe);
 
        try {
            Process process = Runtime.getRuntime().exec(exe);

            try (BufferedReader reader = new BufferedReader(new InputStreamReader(process.getInputStream()))) {
                String line;
                String output = "";
                while ((line = reader.readLine()) != null) {
                    output += (line + "\r\n");
  
                }
                text_Area.setFont( new Font("monospaced", Font.PLAIN, 12) );
                text_Area.setText(output);
            }

        } catch (IOException e) {
        }
    }//GEN-LAST:event_run_ButtonActionPerformed

